      SUBROUTINE TD2SCS(XLIM,NC,XYZ1,XYZ2,IHIT)
      INTEGER NC,IHIT
      REAL XYZ1(*),XYZ2(*),XLIM(2,*)
      REAL FT1,FT2,F1,F2,T,DXY(3)
      FT1=1.
      FT2=0.
      IHIT=4
      DO 10221 IC=1,NC
      DXY(IC) = XYZ1(IC)-XYZ2(IC)
      IF (DXY(IC).EQ.0.) THEN
      IF (SIGN(1.,XYZ1(IC)-XLIM(1,IC)).NE. SIGN(1.,XLIM(2,IC)-XYZ1(IC)))
     * RETURN
      F1=1.
      F2=0.
      ELSE
      F1=(XLIM(1,IC)-XYZ2(IC))/DXY(IC)
      F2=(XLIM(2,IC)-XYZ2(IC))/DXY(IC)
      IF (F1.LT.F2) THEN
      T=F1
      F1=F2
      F2=T
      ENDIF
      ENDIF
      IF (FT1.GT.F1) FT1=F1
      IF (FT2.LT.F2) FT2=F2
      IF (FT2.GT.FT1) RETURN
10221 CONTINUE
10222 CONTINUE
      IHIT=0
      IF (FT1.LT.1.0-0.0000002) THEN
      IHIT = 2
      DO 10231 IC=1,NC
      XYZ1(IC) = XYZ2(IC)+DXY(IC)*FT1
10231 CONTINUE
10232 CONTINUE
      ENDIF
      IF (FT2.gt.0.0000002) THEN
      IHIT = IHIT+1
      DO 10241 IC=1,NC
      XYZ2(IC) = XYZ2(IC)+DXY(IC)*FT2
10241 CONTINUE
10242 CONTINUE
      ENDIF
      END
