      IY0=IYEAR
      IM0=IMONTH-2
      IF (IM0.le.0) THEN
      IM0=IM0+12
      IY0=IY0-1
      ENDIF
      LEAPS0=(IY0)/4-(IY0)/100+(IY0)/400
      D=DAY-IDAY+IDAYS(IM)-IDAYS(IM0)+LEAPS1-LEAPS0
      T=T+D*86400.D0
      IY=IY-IY0
      T=T+IY*31536.D3
