      IF (T1.lt.0) T1=60.D0+T1
      T=T-T1
      SEC=T1
      T1=MOD(T,3600.D0)
      IF (T1.lt.0) T1=3600.D0+T1
      T=T-T1
      FMIN=NINT(T1/60.)
      T1=MOD(T,86400.D0)
      IF (T1.lt.0) T1=86400.D0+T1
      T=T-T1
      HOUR=NINT(T1/3600.D0)
      T=(T-T2TIMF(0.,3.,1.,0.,0.,0.))/86400D0
      T=T+SIGN(0.05D0,T)
      IY=0
      L400=T/146097.D0
      IY=IY+L400*400
      ID=T-L400*146097.D0
      L100=ID/36524
      L100=MIN(MAX(L100,-3),3)
      IY=IY+L100*100
      ID=ID-L100*36524
      L4=ID/1461
      L4=MIN(MAX(L4,-24),24)
      IY=IY+L4*4
      ID=ID-L4*1461
      I=ID/365
      I=MIN(MAX(I,-3),3)
      ID=ID-I*365
      IY=IY+I
      IF (ID.lt.0) THEN
      ID=365+ID
      IY=IY-1
      IF (IY.ne.0 .and. (MOD(IY,4).eq.0 .and. MOD(IY,100).ne.0) .or. MOD
     *(IY,400).eq.0) ID=ID+1
      ENDIF
      MON=1
      IMIN=MIN(MAX(ID/32+1,1),12)
      DO 10011 I=IMIN,12
      IF (ID.ge.IDAYS(I).and.ID.lt.IDAYS(I+1)) THEN
      MON=I
      GOTO 10012
      ENDIF
10011 CONTINUE
10012 CONTINUE
      ID=ID-IDAYS(MON)+1
      MON=MON+2
      IF (MON.gt.12) THEN
      IY=IY+1
      MON=MON-12
      ENDIF
      YEAR=IY
      FMON=MON
      DAY=ID
      END
