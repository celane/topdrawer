C  COMMUNICATION AREA FOR THE LINE SCISSORING AND SHIELDING MODULE.
      SAVE          /UGC000/
C  MAXIMUM NUMBER OF SHIELDS.
      INTEGER       MSHD
      PARAMETER     (MSHD=4)
C  THE DECLARATION OF THE COMMON BLOCK.
      COMMON        /UGC000/
     X              LIMS,
     X              IFLG,PXCD,PYCD,
     X              NSHD,SHLD,
     X              XPNT,YPNT,
     X              NAVL,KAVL,XSEG,YSEG
C  THE SCISSORING LIMITS.
      REAL          LIMS(2,2)
C  FLAG FOR INITIAL POINT ON LINE:
C    0 MEANS INITIAL POINT.
      INTEGER       IFLG
C  COORDINATES OF THE PREVIOUS POINT RETURNED.
      REAL          PXCD,PYCD
C  THE NUMBER OF SHIELDS AVAILABLE.
      INTEGER       NSHD
C  THE SHIELD LIMITS.
      REAL          SHLD(MSHD,2,2)
C  END POINTS OF THE ORIGINAL LINE SEGMENT.
      REAL          XPNT(2),YPNT(2)
C  TOTAL NUMBER OF END POINTS IN THE SCISSORED AND SHIELDED LINE.
      INTEGER       NAVL
C  NUMBER OF END POINTS ALREADY RETURNED.
      INTEGER       KAVL
C  END POINTS OF THE SCISSORED AND SHIELDED LINE.
      REAL          XSEG(2*MSHD+2),YSEG(2*MSHD+2)
