C  COMMUNICATION AREA FOR THE LINE STRUCTURE MODULE.
      SAVE          /UGD000/
C  THE DECLARATION OF THE COMMON BLOCK.
      COMMON        /UGD000/
     X              IFLS,
     X              CMUX,CMUY,
     X              FGAV,FGLO,
     X              SZDS,SZBK,
     X              PNT1,PNT2,
     X              TVL1,TVL2,TVAL,
     X              DELX,DELY
C  LINE STRUCTURE FLAG:
C    1 MEANS "DASHED" LINE,
C    2 MEANS "DOTTED" LINE, AND
C    3 MEANS "DOTDASH" LINE.
      INTEGER       IFLS
C  CENTIMETERS PER UNIT DISTANCE.
      REAL          CMUX,CMUY
C  DATA AVAILABLE FLAG (0 MEANS NONE AVAILABLE).
      INTEGER       FGAV
C  LAST OPERATION FLAG:
C    0 MEANS BLANK VECTOR SUPPLIED,
C    1 MEANS DREW BLANK,
C    2 MEANS DREW DASH, AND
C    3 MEANS DREW POINT.
      INTEGER       FGLO
C  SIZE OF A DASH IN CENTIMETERS.
      REAL          SZDS
C  SIZE OF A BLANK IN CENTIMETERS.
      REAL          SZBK
C  ORIGINAL LINE SEGMENT END POINTS.
      REAL          PNT1(2),PNT2(2)
C  PARAMETER VALUES OF THE ORIGINAL END POINTS IN CENTIMETERS.
      REAL          TVL1,TVL2
C  CURRENT PARAMETER VALUE IN CENTIMETERS.
      REAL          TVAL
C  TANGENT VECTOR ALONG THE LINE SEGMENT IN UNITS PER CENTIMETER.
      REAL          DELX,DELY
