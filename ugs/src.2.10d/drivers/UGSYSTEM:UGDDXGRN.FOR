C  CONTROL BLOCK FOR THE DEVICE-DEPENDENT AREA EXTENSION FOR THE
C  GRINNELL GMR-27 DISPLAY SYSTEM IN A SLAVE DISPLAY MODE.
      SAVE          /UGGR00/
C  NUMBER OF WORDS IN THE OUTPUT BUFFER.
      INTEGER       DDXZ1
      PARAMETER     (DDXZ1=4096)
C  CONTROL BLOCK LENGTH.
      INTEGER       DDXZZ
      PARAMETER     (DDXZZ=2068)
C  THE DECLARATION OF THE COMMON BLOCK.
      COMMON        /UGGR00/
     X              DDXID,
     X              DDXBF,DDXBN,
     X              DDXUN,DDXBT,DDXCT,
     X              DDXCZ,DDXCX,DDXCW,
     X              DDXEM,DDXIL,DDXCR,DDXBL,
     X              DDXAC,DDXXY,DDXBA,
     X              DDXMD,
     X              DDXIO
C  DEVICE-DEPENDENT AREA EXTENSION IDENTIFICATION.
      CHARACTER*8   DDXID
C  STORAGE BUFFER FOR THE OUTPUT BUFFER.
      INTEGER*2     DDXBF(DDXZ1)
C  NUMBER OF WORDS CURRENTLY IN THE OUTPUT BUFFER.
      INTEGER       DDXBN
C  SELECTED UNITS VALUE.
      INTEGER       DDXUN
C  TYPE FOR BLACK AND WHITE MONITORS:
C    TYPES 1 THROUGH 3 ARE SUPPORTED.
      INTEGER       DDXBT
C  TYPE FOR COLOR MONITORS:
C    TYPES 1 THROUGH 4 ARE SUPPORTED.
      INTEGER       DDXCT
C  BASIC CHARACTER SIZE IN RASTER UNITS.
      INTEGER       DDXCZ
C  EXTENDED CHARARACTER SET FLAG:
C    0 MEANS EXTCHR NOT GIVEN, AND
C    1 MEANS EXTCHR GIVEN.
      INTEGER       DDXCX
C  CHARACTER WRITE MODE:
C    0 MEANS REPCHR NOT GIVEN, AND
C    1 MEANS REPCHR GIVEN.
      INTEGER       DDXCW
C  CURRENT ERASE MODE FLAG:
C    0 MEANS DRAW CURRENT SEGMENT, AND
C    1 MEANS ERASE CURRENT SEGMENT.
      INTEGER       DDXEM
C  CURRENT INTENSITY LEVEL VALUE.
      INTEGER       DDXIL
C  CURRENT COLOR VALUE.
      INTEGER       DDXCR
C  CURRENT BLINK MODE VALUE.
      INTEGER       DDXBL
C  CURRENT SIZE OF ACTUAL CHARACTERS.
      INTEGER       DDXAC
C  LAST X-Y COORDINATES.
      INTEGER       DDXXY(2)
C  BUFFERING ACTIVE FLAG:
C    0 MEANS BUFFERING IS NOT ACTIVE, AND
C    1 MEANS BUFFERING IS ACTIVE.
      INTEGER       DDXBA
C  MODE MASKS FOR DISPLAY ORDERS.
      INTEGER*2     DDXMD(4)
C  INPUT/OUTPUT IDENTIFICATION VALUE.
      INTEGER       DDXIO
C  THE FOLLOWING ARRAY OVERLAYS THE ENTIRE DDA EXTENSION.
      INTEGER       DDXRY(DDXZZ)
      EQUIVALENCE   (DDXRY(1),DDXID)
