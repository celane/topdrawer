C  CONTROL BLOCK FOR THE DEVICE-DEPENDENT AREA EXTENSION FOR
C  GENERATING DISPLAY FILES FOR THE TEKTRONIX 4027 COLOR GRAPHICS
C  TERMINAL IN A SEQUENTIAL DATA SET.
      SAVE          /UGUS00/
C  NUMBER OF CHARACTERS IN OUTPUT RECORD.
      INTEGER       DDXZ1
      PARAMETER     (DDXZ1=72)
C  NUMBER OF CHARACTERS IN OUTPUT CARD.
      INTEGER       DDXZ2
      PARAMETER     (DDXZ2=80)
C  CONTROL BLOCK LENGTH.
      INTEGER       DDXZZ
      PARAMETER     (DDXZZ=38)
C  THE DECLARATION OF THE COMMON BLOCK.
      COMMON        /UGUS00/
     X              DDXID,
     X              DDXBF,DDXBN,
     X              DDXPI,DDXPN,DDXPL,DDXDL,
     X              DDXPV,DDXDC,DDXOP,DDXPF,
     X              DDXNL,DDXCG,
     X              DDXCC,DDXCL,DDXCX,DDXPD,
     X              DDXIO
C  DEVICE-DEPENDENT AREA EXTENSION IDENTIFICATION.
      CHARACTER*8   DDXID
C  STORAGE BUFFER FOR THE OUTPUT RECORD.
      CHARACTER*(DDXZ2) DDXBF
C  NUMBER OF CHARACTERS CURRENTLY IN THE OUTPUT RECORD.
      INTEGER       DDXBN
C  PICTURE IDENTIFICATION (ALPHABETIC PART).
      CHARACTER*4   DDXPI
C  PICTURE IDENTIFICATION (NUMERIC PART).
      INTEGER       DDXPN
C  PICTURE ALIAS.
      CHARACTER*8   DDXPL
C  PICTURE ALIAS (DEFERRED VALUE).
      CHARACTER*8   DDXDL
C  PICTURE AVAILABLE FLAG:
C    0 MEANS NO PREVIOUS PICTURE IS AVAILABLE, AND
C    1 MEANS A PICTURE HAS PREVIOUSLY BEEN PRODUCED.
      INTEGER       DDXPV
C  DEFERRED CLEAR FLAG:
C    0 MEANS THE START OF A NEW PICTURE HAS NOT BEEN DEFERRED, AND
C    1 MEANS THE START OF A NEW PICTURE HAS BEEN DEFERRED.
      INTEGER       DDXDC
C  LAST POSITIONING ORDER.
      CHARACTER*8   DDXOP
C  FIRST/SUCCEEDING POINT FLAG.
      INTEGER       DDXPF
C  NUMBER OF LINES IN THE WORK SPACE.
      INTEGER       DDXNL
C  CHARACTER GENERATOR FLAG:
C    0 MEANS GENCH NOT GIVEN, AND
C    1 MEANS GENCH GIVEN.
      INTEGER       DDXCG
C  CURRENT COLOR FLAG.
      CHARACTER*1   DDXCC
C  CURRENT LINE STRUCTURE FLAG.
      CHARACTER*1   DDXCL
C  COMMAND CHARACTER.
      CHARACTER*1   DDXCX
C  PADDING CHARACTER.
      CHARACTER*1   DDXPD
C  INPUT/OUTPUT IDENTIFICATION VALUE.
      INTEGER       DDXIO
C  THE FOLLOWING ARRAY OVERLAYS THE ENTIRE DDA EXTENSION.
      INTEGER       DDXRY(DDXZZ)
      EQUIVALENCE   (DDXRY(1),DDXID)
