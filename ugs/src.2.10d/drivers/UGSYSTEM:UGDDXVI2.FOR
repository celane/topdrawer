C  CONTROL BLOCK FOR THE DEVICE DEPENDENT AREA EXTENSION FOR THE
C  DEC VAXSTATION II/GPX IN THE FULLY INTERACTIVE MODE.
      SAVE          /UGVI00/
C  HIGH X AND Y RASTER LIMIT OF DISPLAY WINDOW.
      INTEGER       DDXZ1
      PARAMETER     (DDXZ1=1048575)
C  SIZE OF THE LINE END POINT ARRAY.
      INTEGER       DDXZ2
      PARAMETER     (DDXZ2=64)
C  CONTROL BLOCK LENGTH.
      INTEGER       DDXZZ
      PARAMETER     (DDXZZ=212)
C  THE DECLARATION OF THE COMMON BLOCK.
      COMMON        /UGVI00/
     X              DDXID,
     X              DDXB1,DDXV2,
     X              DDXVC,DDXVD,DDXKB,DDXWD,
     X              DDXCH,DDXCN,DDXTY,
     X              DDXPE,DDXNE,DDXME,
     X              DDXTR,DDXUX,DDXUY,
     X              DDXSI,DDXSO,DDXPD,DDXGL,
     X              DDXLX,DDXLY,DDXLN,
     X              DDXKX,DDXKY,DDXKD,DDXKS,DDXKN,DDXKC,DDXMC,
     X              DDXKO,DDXKV,
     X              DDXPX,DDXPY,
     X              DDXHF
C  DEVICE-DEPENDENT AREA EXTENSION IDENTIFICATION.
      CHARACTER*8   DDXID
C  POINTER TO SUBROUTINE UGB001.
      INTEGER       DDXB1
C  POINTER TO SUBROUTINE UGVI02.
      INTEGER       DDXV2
C  THE VIRTUAL COLOR MAP IDENTIFICATION.
      INTEGER       DDXVC
C  THE VIRTUAL DISPLAY IDENTIFICATION.
      INTEGER       DDXVD
C  THE KEYBOARD IDENTIFICATION.
      INTEGER       DDXKB
C  THE DISPLAY WINDOW IDENTIFICATION.
      INTEGER       DDXWD
C  CHANNEL IDENTIFICATION AND LENGTH.
      CHARACTER*64  DDXCH
      INTEGER       DDXCN
C  TERMINAL TYPE:
C    0  MEANS MONOCHROME, AND
C    1  MEANS COLOR.
      INTEGER       DDXTY
C  POINTER TO THE SEGMENT TABLE.  EACH ENTRY IN THIS TABLE CONTAINS
C  FOUR WORDS.  THE MEANING OF EACH WORD IS:
C    1  EXTERNAL IDENTIFICATION OF THE SEGMENT.
C    2  INTERNAL IDENTIFICATION OF THE SEGMENT.
C    3  INCLUDE/OMIT STATUS (0 MEANS INCLUDE, 1 MEANS OMIT).
C    4  PICK/NOPICK STATUS (0 MEANS NOPICK, 1 MEANS PICK).
      INTEGER       DDXPE
C  CURRENT AND MAXIMUM NUMBER OF SEGMENTS IN THE SEGMENT TABLE.
      INTEGER       DDXNE,DDXME
C  THE TRANSFORMATION DATA FOR THE SEGMENTS.
      REAL          DDXTR(2,2)
C  EQUIVALENT OF ONE REAL RASTER UNIT IN THE INTERNAL SYSTEM.
      INTEGER       DDXUX,DDXUY
C  THE EXTERNAL IDENTIFICATION OF THE SEGMENT UNDER CONSTRUCTION.
      INTEGER       DDXSI
C  THE OFFSET IN THE X DIRECTION OF THE SEGMENT UNDER CONSTRUCTION.
      REAL          DDXSO
C  PRIVATE DATA FOR THE CURRENT GRAPHIC PRIMITIVE.
      CHARACTER*8   DDXPD
C  GENERATE INTENSITY LEVEL FLAG:
C    0  MEANS DO NOT GENERATE INTENSITY WITH LINE WIDTH, AND
C    1  MEANS GENERATE INTENSITY WITH LINE WIDTH.
      INTEGER       DDXGL
C  THE LINE END POINT ARRAY AND ITS COUNT.
      REAL          DDXLX(DDXZ2),DDXLY(DDXZ2)
      INTEGER       DDXLN
C  COORDINATES OF THE FIRST CHARACTER IN THE KEYBOARD INPUT BUFFER.
      REAL          DDXKX,DDXKY
C  SPACING BETWEEN CHARACTERS IN THE KEYBOARD INPUT BUFFER.
      REAL          DDXKD
C  KEYBOARD INPUT BUFFER AND LENGTH.
      CHARACTER*128 DDXKS
      INTEGER       DDXKN
C  INPUT CHARACTER BUFFERS FOR KEYBOARD AND MOUSE.
      INTEGER       DDXKC,DDXMC
C  OFFSET OF THE KEYBOARD BUTTON SIGNAL.
      INTEGER       DDXKO
C  VALUE OF THE KEYBOARD BUTTON SIGNAL.
      INTEGER       DDXKV
C  COORDINATES OF THE POINTER.
      REAL          DDXPX,DDXPY
C  THE HIBERNATION FLAG:
C   -1  MEANS THE PROGRAM IS NOT IN HIBERNATION,
C    0  MEANS THE PROGRAM IS IN HIBERNATION,
C    1  MEANS A TIME OUT HAS OCCURRED,
C    2  MEANS A KEYBOARD RESPONSE IS AVAILABLE,
C    3  MEANS A SPECIAL KEYBOARD SIGNAL IS AVAILABLE, AND
C    4  MEANS A POINTER VALUE IS AVAILABLE.
      INTEGER       DDXHF
C  THE FOLLOWING ARRAY OVERLAYS THE ENTIRE DDA EXTENSION.
      INTEGER       DDXRY(DDXZZ)
      EQUIVALENCE   (DDXRY(1),DDXID)
