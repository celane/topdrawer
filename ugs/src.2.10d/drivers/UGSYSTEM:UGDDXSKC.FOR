C  CONTROL BLOCK FOR THE DEVICE DEPENDENT AREA EXTENSION FOR THE
C  SEIKO GR-1105 COLOR GRAPHICS TERMINAL AS A RASTER-SCAN DEVICE
C  IN AN INTERACTIVE MODE.
      SAVE          /UGSC00/
C  NUMBER OF CHARACTERS IN THE OUTPUT BUFFER.
      INTEGER       DDXZ1
      PARAMETER     (DDXZ1=1024)
C  NUMBER OF TERMINATION CHARACTERS IN THE OUTPUT BUFFER.
      INTEGER       DDXZ2
      PARAMETER     (DDXZ2=2)
C  CONTROL BLOCK LENGTH.
      INTEGER       DDXZZ
      PARAMETER     (DDXZZ=334)
C  THE DECLARATION OF THE COMMON BLOCK.
      COMMON        /UGSC00/
     X              DDXID,
     X              DDXTY,
     X              DDXBF,DDXBN,
     X              DDXCC,DDXCL,DDXCF,
     X              DDXLX,DDXLY,DDXLP,
     X              DDXBV,DDXLC,
     X              DDXIO,DDXHT,DDXHQ
C  DEVICE-DEPENDENT AREA EXTENSION IDENTIFICATION.
      CHARACTER*8   DDXID
C  THE TERMINAL TYPE:
C    0  MEANS BASIC MODEL, AND
C    1  MEANS VT100 EMULATOR.
      INTEGER       DDXTY
C  STORAGE BUFFER FOR THE OUTPUT BUFFER.
      CHARACTER*(DDXZ1)  DDXBF
C  NUMBER OF CHARACTERS CURRENTLY IN THE OUTPUT BUFFER.
      INTEGER       DDXBN
C  CURRENT DRAWING COLOR, LINE TYPE, AND FILL COLOR.
      INTEGER       DDXCC,DDXCL,DDXCF
C  THE LAST X AND Y COORDINATES (AND PADDING).
      CHARACTER*3   DDXLX,DDXLY
      CHARACTER*2   DDXLP
C  INITIAL BUTTON VALUES.
      CHARACTER*16  DDXBV(16)
C  CURRENT LOCATOR COORDINATES.
      INTEGER       DDXLC(2)
C  INPUT/OUTPUT IDENTIFICATION VALUE.
      INTEGER       DDXIO
C  HIBERNATION FLAG FOR THE INTERVAL TIMER.
      INTEGER       DDXHT
C  HIBERNATION FLAG FOR THE INPUT/OUTPUT PROCESSOR.
      INTEGER       DDXHQ
C  THE FOLLOWING ARRAY OVERLAYS THE ENTIRE DDA EXTENSION.
      INTEGER       DDXRY(DDXZZ)
      EQUIVALENCE   (DDXRY(1),DDXID)
