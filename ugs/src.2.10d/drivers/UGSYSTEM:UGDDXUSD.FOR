C  CONTROL BLOCK FOR THE DEVICE-DEPENDENT AREA EXTENSION FOR THE
C  TEKTRONIX 4027 COLOR GRAPHICS TERMINAL IN A SLAVE DISPLAY MODE.
      SAVE          /UGUD00/
C  NUMBER OF CHARACTERS IN THE OUTPUT BUFFER.
      INTEGER       DDXZ1
      PARAMETER     (DDXZ1=504)
C  NUMBER OF CHARACTERS IN THE INPUT/OUTPUT BUFFER.
      INTEGER       DDXZ2
      PARAMETER     (DDXZ2=512)
C  CONTROL BLOCK LENGTH.
      INTEGER       DDXZZ
      PARAMETER     (DDXZZ=139)
C  THE DECLARATION OF THE COMMON BLOCK.
      COMMON        /UGUD00/
     X              DDXID,
     X              DDXBF,DDXBN,
     X              DDXOP,DDXPF,
     X              DDXNL,DDXCG,DDXEM,
     X              DDXCC,DDXCL,DDXCX,DDXPD,
     X              DDXIO
C  DEVICE-DEPENDENT AREA EXTENSION IDENTIFICATION.
      CHARACTER*8   DDXID
C  STORAGE BUFFER FOR THE INPUT/OUTPUT BUFFER.
      CHARACTER*(DDXZ2) DDXBF
C  NUMBER OF CHARACTERS CURRENTLY IN THE INPUT/OUTPUT BUFFER.
      INTEGER       DDXBN
C  LAST POSITIONING ORDER.
      CHARACTER*8   DDXOP
C  FIRST/SUCCEEDING POINT FLAG.
      INTEGER       DDXPF
C  NUMBER OF LINES IN THE WORK SPACE.
      INTEGER       DDXNL
C  CHARACTER GENERATOR FLAG:
C    0 MEANS GENCH NOT GIVEN, AND
C    1 MEANS GENCH GIVEN.
      INTEGER       DDXCG
C  CURRENT ERASE MODE FLAG:
C    0 MEANS DRAW CURRENT SEGMENT, AND
C    1 MEANS ERASE CURRENT SEGMENT.
      INTEGER       DDXEM
C  CURRENT COLOR FLAG.
      CHARACTER*1   DDXCC
C  CURRENT LINE STRUCTURE FLAG.
      CHARACTER*1   DDXCL
C  COMMAND CHARACTER.
      CHARACTER*1   DDXCX
C  PADDING CHARACTER.
      CHARACTER*1   DDXPD
C  INPUT/OUTPUT IDENTIFICATION VALUE.
      INTEGER       DDXIO
C  THE FOLLOWING ARRAY OVERLAYS THE ENTIRE DDA EXTENSION.
      INTEGER       DDXRY(DDXZZ)
      EQUIVALENCE   (DDXRY(1),DDXID)
