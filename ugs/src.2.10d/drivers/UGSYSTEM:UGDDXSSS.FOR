C  CONTROL BLOCK FOR THE DEVICE DEPENDENT AREA EXTENSION FOR THE
C  SLAC EXPERIMENTAL SLAVE SCOPE.
      SAVE          /UGSX00/
C  NUMBER OF WORDS IN THE INPUT/OUTPUT BUFFER.
      INTEGER       DDXZ1
      PARAMETER     (DDXZ1=1024)
C  DISPLAY BUFFER BEGIN ADDRESS.
      INTEGER       DDXZ2
      PARAMETER     (DDXZ2=0)
C  LENGTH OF THE DISPLAY BUFFER.
      INTEGER       DDXZ3
      PARAMETER     (DDXZ3=1024)
C  CONTROL BLOCK LENGTH.
      INTEGER       DDXZZ
      PARAMETER     (DDXZZ=1049)
C  THE DECLARATION OF THE COMMON BLOCK.
      COMMON        /UGSX00/
     X              DDXID,
     X              DDXB1,DDXS4,DDXS5,DDXS6,
     X              DDXBF,DDXNB,
     X              DDXPE,DDXNE,DDXME,
     X              DDXLS,DDXCR,DDXDM,DDXXY,DDXSD,
     X              DDXCT,DDXXB,DDXXC,
     X              DDXCG,DDXRG
C  DEVICE-DEPENDENT AREA EXTENSION IDENTIFICATION.
      CHARACTER*8   DDXID
C  POINTER TO SUBROUTINE UGB001.
      INTEGER       DDXB1
C  POINTER TO SUBROUTINE UGSX04.
      INTEGER       DDXS4
C  POINTER TO SUBROUTINE UGSX05.
      INTEGER       DDXS5
C  POINTER TO SUBROUTINE UGSX06.
      INTEGER       DDXS6
C  THE INPUT/OUTPUT BUFFER.
      INTEGER*4     DDXBF(DDXZ1)
C  CURRENT SIZE OF INPUT/OUTPUT BUFFER.
      INTEGER       DDXNB
C  POINTER TO THE SEGMENT TABLE.  EACH ENTRY IN THIS TABLE CONTAINS
C  SIX WORDS.  THE MEANING OF EACH WORD IS:
C    1  ENTRY TYPE (0 MEANS INITIAL ENTRY, 1 MEANS USER SEGMENT, 2
C       MEANS TERMINAL ENTRY).
C    2  SEGMENT IDENTIFICATION.
C    3  ORIGIN OF THE SEGMENT IN THE BUFFER.
C    4  LENGTH OF THE SEGMENT IN THE BUFFER.
C    5  NOT USED.
C    6  INCLUDE STATUS (0 MEANS INCLUDE, 1 MEANS OMIT).
      INTEGER       DDXPE
C  CURRENT NUMBER OF SEGMENTS IN THE SEGMENT TABLE.
      INTEGER       DDXNE
C  MAXIMUM NUMBER OF SEGMENTS IN THE SEGMENT TABLE.
      INTEGER       DDXME
C  LINE STRUCTURE GENERATION FLAG.
      INTEGER       DDXLS
C  COLOR GENERATION FLAG.
      INTEGER       DDXCR
C  DISPLAY ORDER MASKS.
      INTEGER*4     DDXDM(3)
C  LAST X-Y COORDINATE.
      INTEGER       DDXXY(2)
C  SEGMENT DATA FOR THE SEGMENT UNDER CONSTRUCTION.
      INTEGER       DDXSD(3)
C  CRATE CONTROLLER FLAG.
      INTEGER       DDXCT
C  BRANCH DRIVER ADDRESS.
      INTEGER*4     DDXXB
C  CAMAC CONTROLLER WORD.
      INTEGER*4     DDXXC
C  CURRENT REGENERATION STATUS.
      INTEGER       DDXCG
C  REQUIRED REGENERATION STATUS.
      INTEGER       DDXRG
C  THE FOLLOWING ARRAY OVERLAYS THE ENTIRE DDA EXTENSION.
      INTEGER       DDXRY(DDXZZ)
      EQUIVALENCE   (DDXRY(1),DDXID)
