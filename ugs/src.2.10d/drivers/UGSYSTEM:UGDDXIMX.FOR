C  CONTROL BLOCK FOR THE DEVICE-DEPENDENT AREA EXTENSION FOR THE
C  MODEL 8/300 PRINTER/PLOTTER OF THE IMAGEN CORPORATION.  THE FILES
C  PRODUCED BY THIS MODULE MUST BE SENT TO THE IBM COMPUTER FOR
C  PROCESSING.
      SAVE          /UGIX00/
C  MAXIMUM NUMBER OF BYTES IN THE OUTPUT RECORD.
      INTEGER       DDXZ1
      PARAMETER     (DDXZ1=152)
C  MAXIMUM NUMBER OF ENTRIES IN COORDINATE STACK.
      INTEGER       DDXZ2
      PARAMETER     (DDXZ2=64)
C  CONTROL BLOCK LENGTH.
      INTEGER       DDXZZ
      PARAMETER     (DDXZZ=180)
C  THE DECLARATION OF THE COMMON BLOCK.
      COMMON        /UGIX00/
     X              DDXID,
     X              DDXRA,
     X              DDXOB,DDXLR,DDXON,
     X              DDXCB,DDXCN,DDXCT,DDXFT,
     X              DDXGL,DDXRI,DDXCI,DDXPV,DDXDC,
     X              DDXIO
C  DEVICE-DEPENDENT AREA EXTENSION IDENTIFICATION.
      CHARACTER*8   DDXID
C  ROTATE AXIS FLAG:
C    0 MEANS DO NOT ROTATE THE PICTURE 90 DEGREES, AND
C    1 MEANS ROTATE THE PICTURE 90 DEGREES.
      INTEGER       DDXRA
C  OUTPUT BUFFER.
      CHARACTER*(DDXZ1) DDXOB
C  ACTUAL LOGICAL RECORD LENGTH.
      INTEGER       DDXLR
C  CURRENT LENGTH OF THE OUTPUT BUFFER.
      INTEGER       DDXON
C  THE COORDINATE STACK.
      INTEGER       DDXCB(2,DDXZ2)
C  CURRENT SIZE OF THE COORDINATE STACK.
      INTEGER       DDXCN
C  CURRENT TYPE OF PATH BEING DRAWN:
C    0 MEANS LINES, AND
C    1 MEANS POLYGON FILL.
      INTEGER       DDXCT
C  CURRENT TYPE OF POLYGON FILL BEING DONE:
C    0 MEANS HOLLOW, AND
C    1 MEANS SOLID.
      INTEGER       DDXFT
C  GENERATE INTENSITY LEVEL FLAG:
C    0 MEANS DO NOT GENERATE INTENSITY WITH LINE WIDTH, AND
C    1 MEANS GENERATE INTENSITY WITH LINE WIDTH.
      INTEGER       DDXGL
C  CURRENT RAW INTENSITY LEVEL.
      INTEGER       DDXRI
C  CURRENT ACTUAL INTENSITY LEVEL.
      INTEGER       DDXCI
C  PICTURE AVAILABLE FLAG:
C    0 MEANS NO PREVIOUS PICTURE IS AVAILABLE, AND
C    1 MEANS A PICTURE HAS PREVIOUSLY BEEN PRODUCED.
      INTEGER       DDXPV
C  DEFERRED CLEAR FLAG:
C    0 MEANS THE START OF A NEW PICTURE HAS NOT BEEN DEFERRED, AND
C    1 MEANS THE START OF A NEW PICTURE HAS BEEN DEFERRED.
      INTEGER       DDXDC
C  INPUT/OUTPUT IDENTIFICATION VALUE.
      INTEGER       DDXIO
C  THE FOLLOWING ARRAY OVERLAYS THE ENTIRE DDA EXTENSION.
      INTEGER       DDXRY(DDXZZ)
      EQUIVALENCE   (DDXRY(1),DDXID)
