C  CONTROL BLOCK FOR THE DEVICE-DEPENDENT AREA EXTENSION FOR THE
C  LINE DRAWING PSEUDO-DEVICE.
      SAVE          /UGPL00/
C  CONTROL BLOCK LENGTH.
      INTEGER       DDXZZ
      PARAMETER     (DDXZZ=16)
C  THE DECLARATION OF THE COMMON BLOCK.
      COMMON        /UGPL00/
     X              DDXID,
     X              DDXDC,
     X              DDXLS,
     X              DDXC1,DDXC2,DDXCR,
     X              DDXCZ,DDXCA,
     X              DDXIO
C  DEVICE-DEPENDENT AREA EXTENSION IDENTIFICATION.
      CHARACTER*8   DDXID
C  DEFERRED CLEAR FLAG:
C    0 MEANS THE START OF A NEW PICTURE HAS NOT BEEN DEFERRED, AND
C    1 MEANS THE START OF A NEW PICTURE HAS BEEN DEFERRED.
      INTEGER       DDXDC
C  PASS LINE STRUCTURE FLAG:
C    0 MEANS GENERATE THE LINE STRUCTURE, AND
C    1 MEANS PASS THE LINE STRUCTURE FLAG ON IN THE GENERATED DATA.
      INTEGER       DDXLS
C  DATA FOR FIRST CHARACTER SIZE.
      INTEGER       DDXC1(3)
C  DATA FOR SECOND CHARACTER SIZE.
      INTEGER       DDXC2(3)
C  DATA FOR ROTATED CHARACTERS.
      INTEGER       DDXCR(3)
C  ACTUAL CHARACTER SIZE.
      INTEGER       DDXCZ
C  ACTUAL CHARACTER ANGLE.
      INTEGER       DDXCA
C  INPUT/OUTPUT IDENTIFICATION VALUE.
      INTEGER       DDXIO
C  THE FOLLOWING ARRAY OVERLAYS THE ENTIRE DDA EXTENSION.
      INTEGER       DDXRY(DDXZZ)
      EQUIVALENCE   (DDXRY(1),DDXID)
