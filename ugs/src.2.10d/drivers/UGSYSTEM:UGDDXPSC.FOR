C  CONTROL BLOCK FOR THE DEVICE-DEPENDENT AREA EXTENSION FOR
C  GENERATING DISPLAY FILES FOR THE POSTSCRIPT LANGUAGE.
      SAVE          /UGPR00/
C  NUMBER OF CHARACTERS IN THE OUTPUT BUFFER.
      INTEGER       DDXZ1
      PARAMETER     (DDXZ1=152)
C  CONTROL BLOCK LENGTH.
      INTEGER       DDXZZ
      PARAMETER     (DDXZZ=112)
C  THE DECLARATION OF THE COMMON BLOCK.
      COMMON        /UGPR00/
     X              DDXID,
     X              DDXBF,DDXBM,DDXBN,
     X              DDXRA,DDXFT,DDXGL,DDXAS,
     X              DDXCI,DDXCC,DDXCL,DDXCT,
     X              DDXLA,DDXSA,DDXSI,DDXSC,
     X              DDXBP,DDXN1,DDXEP,DDXN2,
     X              DDXBX,DDXN3,DDXEX,DDXN4,
     X              DDXBR,DDXN5,DDXER,DDXN6,
     X              DDXPC,DDXPV,DDXDC,
     X              DDXIO
C  DEVICE-DEPENDENT AREA EXTENSION IDENTIFICATION.
      CHARACTER*8   DDXID
C  THE INPUT/OUTPUT BUFFER.
      CHARACTER*(DDXZ1) DDXBF
C  MAXIMUM NUMBER OF CHARACTERS THAT WILL FIT INTO THE INPUT/OUTPUT
C  BUFFER.
      INTEGER       DDXBM
C  NUMBER OF CHARACTERS CURRENTLY IN THE INPUT/OUTPUT BUFFER.
      INTEGER       DDXBN
C  ROTATE AXIS FLAG:
C    0 MEANS DO NOT ROTATE THE PICTURE 90 DEGREES, AND
C    1 MEANS ROTATE THE PICTURE 90 DEGREES.
      INTEGER       DDXRA
C  CURRENT TYPE OF POLYGON FILL BEING DONE:
C    0 MEANS HOLLOW, AND
C    1 MEANS SOLID.
      INTEGER       DDXFT
C  GENERATE INTENSITY LEVEL FLAG:
C    0 MEANS DO NOT GENERATE INTENSITY WITH LINE WIDTH, AND
C    1 MEANS GENERATE INTENSITY WITH LINE WIDTH.
      INTEGER       DDXGL
C  COMPUTER TO ASCII TRANSLATION FLAG:
C    0 MEANS DO NOT DO THE TRANSLATION, AND
C    1 MEANS DO THE TRANSLATION.
      INTEGER       DDXAS
C  CURRENT INTENSITY LEVEL.
      INTEGER       DDXCI
C  CURRENT COLOR.
      INTEGER       DDXCC
C  CURRENT LINE STRUCTURE.
      INTEGER       DDXCL
C  CURRENT TEXT SIZE.
      INTEGER       DDXCT
C  LINE ACTIVE FLAG:
C    0 MEANS NO LINE IS ACTIVE, AND
C    1 MEANS A LINE IS ACTIVE.
      INTEGER       DDXLA
C  SAVED ANGLE FOR TEXT.
      INTEGER       DDXSA
C  SAVED INTENSITY LEVEL FOR POLYGON FILL.
      INTEGER       DDXSI
C  SAVED COLOR FOR POLYGON FILL.
      INTEGER       DDXSC
C  BEGINNING OF PROGRAM STRING AND ITS LENGTH.
      CHARACTER*32  DDXBP
      INTEGER       DDXN1
C  END OF PROGRAM STRING AND ITS LENGTH.
      CHARACTER*32  DDXEP
      INTEGER       DDXN2
C  BEGINNING OF PICTURE STRING AND ITS LENGTH.
      CHARACTER*32  DDXBX
      INTEGER       DDXN3
C  END OF PICTURE STRING AND ITS LENGTH.
      CHARACTER*32  DDXEX
      INTEGER       DDXN4
C  BEGINNING OF RECORD STRING AND ITS LENGTH.
      CHARACTER*32  DDXBR
      INTEGER       DDXN5
C  END OF RECORD STRING AND ITS LENGTH.
      CHARACTER*32  DDXER
      INTEGER       DDXN6
C  PICTURE COUNT.
      INTEGER       DDXPC
C  PICTURE AVAILABLE FLAG:
C    0 MEANS NO PREVIOUS PICTURE IS AVAILABLE, AND
C    1 MEANS A PICTURE HAS PREVIOUSLY BEEN PRODUCED.
      INTEGER       DDXPV
C  DEFERRED CLEAR FLAG:
C    0 MEANS THE START OF A NEW PICTURE HAS NOT BEEN DEFERRED, AND
C    1 MEANS THE START OF A NEW PICTURE HAS BEEN DEFERRED.
      INTEGER       DDXDC
C  INPUT/OUTPUT IDENTIFICATION VALUE.
      INTEGER       DDXIO
C  THE FOLLOWING ARRAY OVERLAYS THE ENTIRE DDA EXTENSION.
      INTEGER       DDXRY(DDXZZ)
      EQUIVALENCE   (DDXRY(1),DDXID)
