C  COMMUNICATION AREA FOR THE THREE-DIMENSIONAL LINE SCISSORING
C  MODULE.
      SAVE          /UGG000/
C  THE DECLARATION OF THE COMMON BLOCK.
      COMMON        /UGG000/
     X              ITYP,
     X              EQUN,LIMS,
     X              IFLG,PXCD,PYCD,PZCD,
     X              XPNT,YPNT,ZPNT,
     X              NAVL,KAVL,XSEG,YSEG,ZSEG
C  TYPE OF SCISSORING REQUIRED:
C    0 MEANS PLANAR, AND
C    1 MEANS RECTANGULAR PARALLELOPIPED.
      INTEGER       ITYP
C  THE EQUATION OF THE SCISSORING PLANE.
      REAL          EQUN(4)
C  THE RECTANGULAR SCISSORING LIMITS.
      REAL          LIMS(3,2)
C  FLAG FOR INITIAL POINT ON LINE:
C    0 MEANS INITIAL POINT.
      INTEGER       IFLG
C  COORDINATES OF THE PREVIOUS POINT RETURNED.
      REAL          PXCD,PYCD,PZCD
C  END POINTS OF THE ORIGINAL LINE SEGMENT.
      REAL          XPNT(2),YPNT(2),ZPNT(2)
C  TOTAL NUMBER OF END POINTS IN THE SCISSORED LINE.
      INTEGER       NAVL
C  NUMBER OF END POINTS ALREADY RETURNED.
      INTEGER       KAVL
C  END POINTS OF THE SCISSORED LINE.
      REAL          XSEG(2),YSEG(2),ZSEG(2)
